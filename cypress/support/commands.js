// Introduces a delay after each command
const COMMAND_DELAY = 400;

const DELAYED_COMMANDS = [
  'blur',
  'check',
  'clear',
  'click',
  'contains',
  'dblclick',
  'focus',
  'reload',
  'rightclick',
  'select',
  'submit',
  'trigger',
  'type',
  'uncheck',
  'visit',
];

for (const command of DELAYED_COMMANDS) {
  Cypress.Commands.overwrite(command, (originalFn, ...args) => {
    const origVal = originalFn(...args);

    return new Promise((resolve) => {
      setTimeout(() => {
        resolve(origVal);
      }, COMMAND_DELAY);
    });
  });
}
